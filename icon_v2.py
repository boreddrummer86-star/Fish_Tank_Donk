def get_splash_screen(width: int = 80, height: int = 24) -> List[str]:
    """
    Generate a splash screen sized for the terminal.

    Args:
        width: Terminal width
        height: Terminal height

    Returns:
        List of strings for each line
    """
    lines = []

    # Top border
    lines.append("â•" * width)
    lines.append("")

    # Title
    title = "FISH TANK DONK v2.0"
    lines.append(title.center(width))
    lines.append("â”" * len(title).center(width))
    lines.append("")

    # Subtitle
    subtitle = "3D Wireframe Aquarium Simulator"
    lines.append(subtitle.center(width))
    lines.append("")

    # Creatures
    lines.append("â•­â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â•®".center(width))
    lines.append("â”‚   ğŸ¦ˆ SHARK          ğŸª¼ JELLYFISH     â”‚".center(width))
    lines.append("â”‚   Press [1]         Press [2]        â”‚".center(width))
    lines.append("â•°â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â•¯".center(width))
    lines.append("")

    # Instructions
    lines.append("Press any key to continue...".center(width))
    lines.append("")

    # Bottom border
    lines.append("â•" * width)

    # Pad to height
    while len(lines) < height:
        lines.insert(1, "")

    return lines[:height]


